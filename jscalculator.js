var opr = prompt('operator: ')
var num1 = prompt("firstnum: ")
var num2 = prompt("secondnum: ")

if (opr == "+")
{
    console.log(num1 + num2)
} else if (opr == "-")
{
    console.log(num1 - num2)
} else if (opr == "*")
{
    console.log(num1 * num2)
} else if (opr == "/")
{
    console.log(num1 / num2)
}